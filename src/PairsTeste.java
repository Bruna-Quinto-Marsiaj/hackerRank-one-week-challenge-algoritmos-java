    public static int pairs(int k, List<Integer> numbers) {
        // Write your code here
        Set<Integer> uniqueNumbers = new HashSet<>(numbers);
        int count = 0;
        for(int num : uniqueNumbers){
            if(uniqueNumbers.contains(num+k)){
                count++;
            }
        }
        return count;

    }

